import React, { useState } from 'react';
import FileSelector from '../FileSelector/FileSelector';
import UnloadingWars from './UnloadingWars/UnloadingWars';
import './sm.css';
import UnloadingTitans from './UnloadingTitans/UnloadingTitans';

const AVAILABLE_FILES = [
  'зомби 09.02 герои.txt',
  'зомби 19.02 герои.txt',
  'поляки 12.02 герои.txt',
  'поляки 20.02 герои.txt'
];
const AVAILABLE_FILES_2 = [
  'зомби 09.02 титаны.txt',
  'зомби 19.02 титаны.txt',
  'поляки 12.02 титаны.txt',
  'поляки 20.02 титаны.txt'
];
const ALL_FILES = [...AVAILABLE_FILES, ...AVAILABLE_FILES_2];

const Sm = () => {
   const [activeFile, setActiveFile] = useState(null);
 
   const handleFileSelect = (file) => {
     setActiveFile(prev => prev === file ? null : file);
   };
 
   // Определяем, файл с титанами или героями
   const isTitansFile = activeFile && activeFile.includes('титаны');
 
   return (
     <div className="conteiner">
      <header className="header">
     
        <p className="abzac">
        
         
   
          <p>
          Временно свободная атака на всех СМ. В первый день СМ идем по стандарту: мост, бастион на выбор,мост героев,храм на выбор,цитадель.После захвата этих укреплений забираем иные с приоритетом захвата укреплений полностью-зависит от сложности противника.
          </p>
          <p>
          Каждый,являясь"командующим", выбирает себе цели сам. Все атаки на пачки, приближенные к максималке, прогоняем через тесты в тренировочном чате СМ. Если цель пока недоступна из-за временно невзятых укреплений до укрепления с желаемой целью, ставим сами себе метку, давая понять остальным,что цель занята другим игроком,чтобы остальные не теряли время на подбор пачки для атаки на вашу цель,когда она станет доступной для атаки.
          </p>
          <p>
            Бестолковые атаки для галочки будут означать,что кому-то пора в бестолковую гильдию. У игрока, не сделавшего 2 атаки титанами и 3 пачками героев, внезапно сильно повышаются шансы быть занесенными в картотеку пропавших из гильдии без вести. Если у вас конченный второй пак титанов или уебанская третья пачка героев, подумайте,куда их слить с пользой для всех-можно разрядить заряженного титана/героя, попробовать добить обглотки и лохмотья недобитой цели.
          </p>
          <p>Игрок, желающий подняться с колен в глазах остальных игроков за свой косяк, может попросить нашего смотрящего за СМ Rom выбрать для себя наказание и с чувством утраченной радости жизни преисполниться и выполнить его во всем своем безобразии. </p>
          <p>
            В случае протовостояния с сильным противником сообщаем в любой чат для остальных полезную информацию о том,как громко пукнул ваш отряд, с какой тональностью и тембром свиста, например, фото петушиной атаки в телегу или сообщение в чат игры о заряженной тенебрис,чтобы не пересматривать еблоатаку другим игрокам, готовясь добить начатую точку,чтобы понимать примерное начало боя. 
          </p>
        </p>
      </header>
       <FileSelector 
         files={ALL_FILES}
         activeFile={activeFile}
         onFileSelect={handleFileSelect}
       />
 
       {activeFile && (
         isTitansFile 
           ? <UnloadingTitans fileName={activeFile} />
           : <UnloadingWars fileName={activeFile} />
       )}
 

    </div>
  );
};

export default Sm;
